public class Calculator {
    public static int add(int num1, int num2){
        return num1 + num2;
    }

    public static int subtract(int i, int i1) {
        return i - i1;
    }


    public static int multiply(int i, int i1) {
        return i * i1;
    }

    public static double divide(double i, double i1) {
        return i / i1;
    }
}
